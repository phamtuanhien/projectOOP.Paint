    @Override
    public void paintComponent(Graphics g) {
        super.paintComponent(g);
        doDrawing(g);      
    }

    @Override
    public void mouseClicked(MouseEvent e) {
        switch(mode) {
            case "BUCKET":
                bucket.setClick(e.getPoint());
                bucket.setArrPoint(endPoint);
                bucket.setColor(Color.BLACK);
                bucket.draw(buff_img);
                
        }
        repaint();
    }

    @Override
    public void mousePressed(MouseEvent e) {
        startPoint = e.getPoint();
        repaint();
    }
    @Override
    public void mouseReleased(MouseEvent e) {
        startPoint = null;
        endPoint = null;
        switch(mode) {
            case "LINE":
                line.draw(g2d);
        }
        repaint();
    }

    @Override
    public void mouseEntered(MouseEvent e) {
        
    }

    @Override
    public void mouseExited(MouseEvent e) {
        jCoordinate.setText("");
    }

    @Override
    public void mouseDragged(MouseEvent e) {
        jCoordinate.setText(e.getX() + ", " + e.getY() + " px");
        endPoint = e.getPoint();
        switch(mode) {
            case "SELECT":
                break;
            case "PENCIL":
                pencil.setPoint(startPoint, endPoint);
                pencil.addArrPoint(endPoint);
                startPoint = endPoint;       
                pencil.setStrokeColor(Color.BLACK);
                pencil.draw(g2d);
                break;
            case "COLORPICKER":
                break;
            case "ERASER":
                break;
            case "TEXT":
                break;
            case "BUCKET":
                break;
            case "MAGNIFIER":
                break;
            case "CURVE":
                break;
            case "HEXAGON":
                break;
            case "LINE":
                line.setPoint(startPoint, endPoint);
                line.setStrokeColor(Color.BLACK);
                break;
            case "OVAL":
                break;
            case "RECTANGLE":
                break;
            case "ROUNDRECTANGLE":
                break;
            case "TRIANGLE":
                break;
            case "RIGHTTRIANGLE":
                break;
        }
        repaint();
    }

    @Override
    public void mouseMoved(MouseEvent e) {
        jCoordinate.setText(e.getX() + ", " + e.getY() + " px");
        
    }
    
    
    
